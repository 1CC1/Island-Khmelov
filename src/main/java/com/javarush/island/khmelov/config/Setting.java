    public Map<String, Map<String, Integer>> odds;

    public Setting() {
        odds = new HashMap<>();
        fillDefaultValue();
    }

    private void fillDefaultValue() {
        for (int i = 0, n = names.length; i < n; i++) {
            String key = names[i];
            this.odds.putIfAbsent(key, new HashMap<>());
            for (int j = 0; j < n; j++) {
                int ratio = probTable[i][j];
                this.odds.get(key).put(names[j], ratio);
            }
        }
    }

    public Map<String, Integer> getOdds(String keyName) {
        return odds.get(keyName);
    }
